def chunk_froala_html(
    html_str: str,
    doc_id: str,
    config: ParseConfig | None = None,
) -> list[Chunk]:
    """Parse Froala 3.2.0 HTML and produce RAG-ready chunks.

    This is the single public entry point.
    """
    if config is None:
        config = ParseConfig()

    soup = sanitize_and_normalize_html(html_str)
    soup = normalize_froala_structure(soup)

    root = soup.find("div", id="_root") or soup.find("div") or soup
    hs: dict[str, list] = {"path": [], "levels": []}
    blocks = extract_blocks(root, hs, config)

    chunks = chunk_sections(blocks, doc_id, config)

    for i, c in enumerate(chunks):
        c.metadata.setdefault("source_order", i)

    return chunks


