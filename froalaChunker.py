#  6. Section Chunking
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━


def chunk_sections(
    blocks: list[dict],
    doc_id: str,
    config: ParseConfig,
) -> list[Chunk]:
    """Turn extracted blocks into Chunks. Tables are delegated out."""
    chunks: list[Chunk] = []
    buf: list[dict] = []
    order = 0

    def flush() -> None:
        nonlocal buf, order
        if not buf:
            return
        new = _build_section_chunks(buf, doc_id, config, order)
        chunks.extend(new)
        order += len(new)
        buf = []

    for blk in blocks:
        if blk["type"] == "table":
            flush()
            tc = _process_table(blk["tag"], blk["heading_path"], doc_id, config, order)
            chunks.extend(tc)
            order += len(tc)
            continue

        if blk["type"] == "heading":
            flush()

        buf.append(blk)

        # budget check
        buf_text = "\n".join(b["text"] for b in buf if b["type"] != "heading")
        if estimate_tokens(buf_text) >= config.max_tokens:
            flush()

    flush()
    return chunks


def _build_section_chunks(
    blocks: list[dict],
    doc_id: str,
    config: ParseConfig,
    start_order: int,
) -> list[Chunk]:
    """Convert a contiguous run of blocks into ≥1 section Chunk(s),
    splitting at block boundaries when the token budget overflows.

    Headings contribute to heading_path / metadata but are **not**
    repeated as body text (avoids duplication).
    """
    if not blocks:
        return []

    hp = blocks[0].get("heading_path", [])

    # Separate heading blocks from body blocks
    body = [b for b in blocks if b["type"] != "heading"]
    if not body:
        # heading-only section (e.g. heading with no following content yet)
        return []

    all_text = "\n".join(b["text"] for b in body)
    all_html = "\n".join(b.get("html", "") for b in body)
    btypes = list({b["type"] for b in body})
    dom_paths = [b.get("dom_path", "") for b in body if b.get("dom_path")]

    tok = estimate_tokens(all_text)
    if tok <= config.max_tokens:
        return [_make_section(hp, all_text, all_html, btypes, dom_paths, doc_id, start_order)]

    # ── split at block boundaries ──────────────────────────────────
    result: list[Chunk] = []
    sub: list[dict] = []
    sub_tok = 0

    for b in body:
        bt = estimate_tokens(b["text"])
        if sub and sub_tok + bt > config.max_tokens:
            result.append(
                _make_section(
                    hp,
                    "\n".join(s["text"] for s in sub),
                    "\n".join(s.get("html", "") for s in sub),
                    list({s["type"] for s in sub}),
                    [s.get("dom_path", "") for s in sub if s.get("dom_path")],
                    doc_id,
                    start_order + len(result),
                )
            )
            sub, sub_tok = [], 0
        sub.append(b)
        sub_tok += bt

    if sub:
        result.append(
            _make_section(
                hp,
                "\n".join(s["text"] for s in sub),
                "\n".join(s.get("html", "") for s in sub),
                list({s["type"] for s in sub}),
                [s.get("dom_path", "") for s in sub if s.get("dom_path")],
                doc_id,
                start_order + len(result),
            )
        )
    return result


def _make_section(
    hp: list[str],
    text: str,
    html_str: str,
    btypes: list[str],
    dom_paths: list[str],
    doc_id: str,
    order: int,
) -> Chunk:
    prefix = _heading_prefix(hp)
    embed = f"{prefix}\n\n{text}".strip() if prefix else text
    return Chunk(
        chunk_id=f"{doc_id}_{_uid()}",
        doc_id=doc_id,
        chunk_type="section",
        heading_path=hp,
        embedding_text=embed,
        cleaned_html=html_str,
        plain_text=text,
        metadata={
            "block_types": btypes,
            "token_estimate": estimate_tokens(text),
            "source_order": order,
            "dom_path": dom_paths[:3] if dom_paths else [],
        },
    )


# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
#  7. Table Router
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━


def _process_table(
    table_tag: Tag,
    heading_path: list[str],
    doc_id: str,
    config: ParseConfig,
    start_order: int,
) -> list[Chunk]:
    """Classify and dispatch to the correct table parser."""
    if not config.detect_layout_tables:
        return parse_data_table(table_tag, heading_path, doc_id, config)

    kind, reason = classify_table(table_tag)

    if kind == "data":
        chunks = parse_data_table(table_tag, heading_path, doc_id, config)
    elif kind == "layout":
        chunks = parse_layout_table(table_tag, heading_path, doc_id, config)
    else:
        chunks = parse_layout_table(table_tag, heading_path, doc_id, config)
        for c in chunks:
            c.metadata["table_kind"] = "unknown"

    for i, c in enumerate(chunks):
        c.metadata.setdefault("table_kind", kind)
        c.metadata["table_confidence"] = reason["confidence"]
        c.metadata["classification_reason"] = reason
        c.metadata.setdefault("source_order", start_order + i)
    return chunks


# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
#  8. Data Table Parser
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━


def parse_data_table(
    table_tag: Tag,
    heading_path: list[str],
    doc_id: str,
    config: ParseConfig,
) -> list[Chunk]:
    """Parse a data table into one summary chunk + one chunk per body row.

    Each row chunk repeats the column headers so it is self-contained.
    """
    chunks: list[Chunk] = []
    prefix = _heading_prefix(heading_path)

    # caption
    cap_tag = table_tag.find("caption")
    caption = _tag_text(cap_tag) if cap_tag else ""

    # headers
    headers = _extract_headers(table_tag)

    # body rows (skip header-only rows)
    data_rows = _extract_data_rows(table_tag, headers)

    # ── summary chunk ──────────────────────────────────────────────
    lines: list[str] = []
    if prefix:
        lines.append(prefix)
    lines.append(f"[Table] {caption}" if caption else "[Table]")
    if headers:
        lines.append("[Headers] " + " | ".join(headers))
    for i, cells in enumerate(data_rows[:5]):
        lines.append("[Row] " + " | ".join(cells))
    if len(data_rows) > 5:
        lines.append(f"... ({len(data_rows)} rows total)")
    summary_text = "\n".join(lines)

    chunks.append(
        Chunk(
            chunk_id=f"{doc_id}_{_uid()}",
            doc_id=doc_id,
            chunk_type="data_table_section",
            heading_path=heading_path,
            embedding_text=summary_text,
            cleaned_html=_tag_html(table_tag),
            plain_text=_tag_text(table_tag),
            metadata={
                "block_types": ["table"],
                "has_table": True,
                "table_kind": "data",
                "caption": caption,
                "headers": headers,
                "row_count": len(data_rows),
                "token_estimate": estimate_tokens(summary_text),
                "dom_path": _dom_path(table_tag),
            },
        )
    )

    # ── per-row chunks ─────────────────────────────────────────────
    for idx, cells in enumerate(data_rows):
        rlines: list[str] = []
        if prefix:
            rlines.append(prefix)
        if caption:
            rlines.append(f"[Table] {caption}")
        if headers:
            rlines.append("[Headers] " + " | ".join(headers))
        if headers and len(headers) == len(cells):
            rlines.append("[Row] " + " | ".join(f"{h}: {v}" for h, v in zip(headers, cells)))
        else:
            rlines.append("[Row] " + " | ".join(cells))
        row_text = "\n".join(rlines)

        chunks.append(
            Chunk(
                chunk_id=f"{doc_id}_{_uid()}",
                doc_id=doc_id,
                chunk_type="data_table_row",
                heading_path=heading_path,
                embedding_text=row_text,
                cleaned_html="",  # individual row HTML not critical
                plain_text=" | ".join(cells),
                metadata={
                    "block_types": ["table_row"],
                    "has_table": True,
                    "table_kind": "data",
                    "row_index": idx,
                    "headers": headers,
                    "caption": caption,
                    "token_estimate": estimate_tokens(row_text),
                },
            )
        )

    return chunks


def _extract_headers(table_tag: Tag) -> list[str]:
    """Get column headers from <thead> or the first <th>-only row."""
    thead = table_tag.find("thead")
    if thead:
        hrow = thead.find("tr")
        if hrow:
            return [_tag_text(c) for c in hrow.find_all(["th", "td"], recursive=False)]
    first_row = table_tag.find("tr")
    if first_row:
        ths = first_row.find_all("th", recursive=False)
        if ths and len(ths) == len(first_row.find_all(["th", "td"], recursive=False)):
            return [_tag_text(th) for th in ths]
    return []


def _extract_data_rows(table_tag: Tag, headers: list[str]) -> list[list[str]]:
    """Return body rows as lists of cell strings, skipping the header row."""
    tbody = table_tag.find("tbody")
    parent = tbody if tbody else table_tag
    rows: list[list[str]] = []
    for tr in parent.find_all("tr", recursive=False):
        cells = tr.find_all(["td", "th"], recursive=False)
        texts = [_tag_text(c) for c in cells]
        # skip if this looks like the header row
        if headers and texts == headers:
            continue
        # skip all-th rows (likely a repeated header)
        if all(c.name == "th" for c in cells) and headers:
            continue
        rows.append(texts)
    return rows


# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
#  9. Layout Table Parser
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━


def parse_layout_table(
    table_tag: Tag,
    heading_path: list[str],
    doc_id: str,
    config: ParseConfig,
) -> list[Chunk]:
    """Linearise a layout table.  Does NOT produce row/column chunks.

    Instead, each top-level cell's content is extracted in DOM order
    as a ``layout_region`` chunk.  Nested tables are recursively
    classified.
    """
    chunks: list[Chunk] = []

    # Find cells belonging directly to this table
    top_cells: list[Tag] = [
        cell
        for cell in table_tag.find_all(["td", "th"], recursive=True)
        if cell.find_parent("table") is table_tag
    ]

    for ci, cell in enumerate(top_cells):
        nested = cell.find_all("table", recursive=False)

        # Always detect cell-level headings first
        cell_hp = _detect_cell_headings(cell, heading_path)

        if nested:
            # For each nested table, build a heading_path from headings
            # that appear before that table in DOM order
            for nt in nested:
                nt_hp = _headings_before_element(cell, nt, heading_path)
                nk, nr = classify_table(nt)
                if nk == "data":
                    nc = parse_data_table(nt, nt_hp, doc_id, config)
                else:
                    nc = parse_layout_table(nt, nt_hp, doc_id, config)
                for c in nc:
                    c.metadata["nested_table"] = True
                    c.metadata.setdefault("table_kind", nk)
                    c.metadata["classification_reason"] = nr
                chunks.extend(nc)

            # Non-table remnants in the cell
            remnant = _extract_non_table_content(cell)
            if remnant.strip():
                role = _guess_region_role(cell, remnant)
                chunks.append(_layout_region(cell_hp, remnant, cell, ci, role, doc_id))
        else:
            text = _tag_text(cell)
            if not text.strip():
                continue
            role = _guess_region_role(cell, text)
            chunks.append(_layout_region(cell_hp, text, cell, ci, role, doc_id))

    return chunks


def _layout_region(
    hp: list[str],
    text: str,
    cell: Tag,
    cell_idx: int,
    role: str,
    doc_id: str,
) -> Chunk:
    prefix = _heading_prefix(hp)
    embed = f"{prefix}\n\n{text}".strip() if prefix else text
    return Chunk(
        chunk_id=f"{doc_id}_{_uid()}",
        doc_id=doc_id,
        chunk_type="layout_region",
        heading_path=hp,
        embedding_text=embed,
        cleaned_html=_tag_html(cell),
        plain_text=text,
        metadata={
            "block_types": ["layout_cell"],
            "table_kind": "layout",
            "region_role": role,
            "cell_index": cell_idx,
            "token_estimate": estimate_tokens(text),
            "dom_path": _dom_path(cell),
        },
    )


def _detect_cell_headings(cell: Tag, parent_hp: list[str]) -> list[str]:
    """Build heading_path for a layout cell.

    Starts from the parent heading_path, then appends any headings
    found **inside** this cell.
    """
    hp = list(parent_hp)
    for h in cell.find_all(list(HEADING_TAGS)):
        lvl = _heading_level(h.name)
        txt = _tag_text(h)
        if txt:
            # trim to level
            while hp:
                existing_lvl = int(hp[-1][2]) if len(hp[-1]) > 2 and hp[-1][1] == "H" and hp[-1][2].isdigit() else 0
                if existing_lvl >= lvl:
                    hp.pop()
                else:
                    break
            hp.append(f"[H{lvl}] {txt}")
    return hp


def _headings_before_element(cell: Tag, target: Tag, parent_hp: list[str]) -> list[str]:
    """Collect headings inside *cell* that appear before *target* in DOM order.

    Used to build the heading_path for a nested table so that headings
    like ``<h2>Products</h2>`` appearing before the table are included.
    """
    hp = list(parent_hp)
    for el in cell.descendants:
        if el is target:
            break
        if isinstance(el, Tag) and el.name in HEADING_TAGS:
            lvl = _heading_level(el.name)
            txt = _tag_text(el)
            if txt:
                while hp:
                    existing_lvl = int(hp[-1][2]) if len(hp[-1]) > 2 and hp[-1][1] == "H" and hp[-1][2].isdigit() else 0
                    if existing_lvl >= lvl:
                        hp.pop()
                    else:
                        break
                hp.append(f"[H{lvl}] {txt}")
    return hp


def _extract_non_table_content(cell: Tag) -> str:
    """Get text from a cell excluding nested <table> subtrees."""
    parts: list[str] = []
    for child in cell.children:
        if isinstance(child, Tag) and child.name == "table":
            continue
        if isinstance(child, Tag):
            parts.append(_tag_text(child))
        elif isinstance(child, NavigableString):
            t = str(child).strip()
            if t:
                parts.append(t)
    return " ".join(p for p in parts if p)


def _guess_region_role(cell: Tag, text: str) -> str:
    """Heuristic region role: nav | aside | main | boilerplate | unknown."""
    links = cell.find_all("a", recursive=True)
    link_len = sum(len(_tag_text(a)) for a in links)
    ld = link_len / max(len(text), 1)

    if ld > 0.6 and len(links) >= 3:
        avg = link_len / max(len(links), 1)
        return "nav" if avg < 30 else "boilerplate"

    if len(text) < 30:
        return "boilerplate"

    if cell.find(list(HEADING_TAGS)) or len(text) > 200:
        return "main"

    if cell.find_all("img", recursive=True) and len(text) < 100:
        return "aside"

    return "unknown"


