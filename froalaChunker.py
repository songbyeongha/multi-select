#!/usr/bin/env python3
"""
Froala Editor 3.2.0 HTML Chunker v2 for RAG / Search Indexing
==============================================================

Parses HTML generated by Froala Editor 3.2.0, producing semantically
meaningful chunks with heading-path context, data-table row expansion,
and layout-table linearisation.

Key improvements over v1
------------------------
- Heading text no longer duplicated in embedding_text
- Layout-table cells propagate internal headings into heading_path
- dom_path tracked in metadata
- BR-paragraph restoration preserves inline markup
- classify_table excludes nested-table signals via _find_own()
- Robust error handling throughout

Dependencies: beautifulsoup4, lxml
"""

from __future__ import annotations

import html as html_mod
import re
import uuid
from dataclasses import dataclass, field
from typing import Any

from bs4 import BeautifulSoup, Comment, NavigableString, Tag

# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
#  1. Data Structures
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━


@dataclass
class ParseConfig:
    """Knobs for the chunker."""

    target_tokens: int = 400
    max_tokens: int = 550
    min_tokens: int = 120
    keep_html: bool = True
    detect_layout_tables: bool = True
    split_on_paragraph_only: bool = True


@dataclass
class Chunk:
    """A single chunk produced by the chunker."""

    chunk_id: str
    doc_id: str
    chunk_type: str  # section | data_table_section | data_table_row | layout_region
    heading_path: list[str]
    embedding_text: str
    cleaned_html: str
    plain_text: str
    metadata: dict[str, Any] = field(default_factory=dict)


# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
#  2. Constants & Tiny Helpers
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

HEADING_TAGS = frozenset({"h1", "h2", "h3", "h4", "h5", "h6"})
BLOCK_TAGS = HEADING_TAGS | frozenset(
    {
        "p", "div", "ul", "ol", "li", "pre", "blockquote",
        "table", "img", "hr", "figure", "figcaption",
        "section", "article", "header", "footer", "nav", "aside", "main",
        "details", "summary", "dl", "dt", "dd",
    }
)
REMOVE_TAGS = frozenset({"script", "style", "noscript", "template", "iframe"})


def _uid() -> str:
    return uuid.uuid4().hex[:12]


def estimate_tokens(text: str) -> int:
    """Rough token count for mixed Korean / English text (~4 chars/token)."""
    if not text:
        return 0
    return max(1, len(text) // 4)


def _heading_level(name: str) -> int:
    """Return 1–6 for h1–h6, else 0."""
    if name and name.lower() in HEADING_TAGS:
        return int(name[1])
    return 0


def _tag_text(tag: Tag) -> str:
    """Visible text, whitespace-normalised."""
    return re.sub(r"\s+", " ", tag.get_text(separator=" ")).strip()


def _tag_html(tag: Tag) -> str:
    return str(tag)


def _dom_path(tag: Tag) -> str:
    """Build a CSS-like DOM path for debugging, e.g. 'html>body>div>table>tr>td'."""
    parts: list[str] = []
    node = tag
    while node and isinstance(node, Tag):
        parts.append(node.name)
        node = node.parent  # type: ignore[assignment]
    parts.reverse()
    return ">".join(parts)


def _heading_prefix(heading_path: list[str]) -> str:
    """Build the heading-path string for embedding_text."""
    return "\n".join(heading_path)


def _normalise_ws(text: str) -> str:
    return re.sub(r"[ \t]+", " ", text).strip()


# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
#  3. HTML Sanitisation & Froala Normalisation
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━


def sanitize_and_normalize_html(html_str: str) -> BeautifulSoup:
    """Parse raw HTML, extract <body> if present, strip junk tags.

    Returns a BeautifulSoup tree wrapped in a single <div> container.
    """
    soup = BeautifulSoup(html_str, "lxml")

    # Extract body content (Froala fullPage mode)
    body = soup.find("body")
    source = body if body else soup

    wrapper = BeautifulSoup("<div id='_root'></div>", "lxml").find("div")
    assert wrapper is not None
    for child in list(source.children):
        wrapper.append(child.extract())

    new_soup = BeautifulSoup(str(wrapper), "lxml")

    # Strip unwanted tags
    for name in REMOVE_TAGS:
        for tag in new_soup.find_all(name):
            tag.decompose()

    # Strip HTML comments
    for c in new_soup.find_all(string=lambda t: isinstance(t, Comment)):
        c.extract()

    return new_soup


def normalize_froala_structure(soup: BeautifulSoup) -> BeautifulSoup:
    """Froala 3.2.x specific normalisation.

    - Convert double-<br> into <p> boundaries
    - Remove empty tags
    - Normalise whitespace
    """
    _convert_double_br(soup)
    _strip_empty_tags(soup)
    # Normalise whitespace in text nodes (skip <pre>/<code>)
    for tn in soup.find_all(string=True):
        if tn.parent and tn.parent.name in ("pre", "code"):
            continue
        cleaned = _normalise_ws(str(tn))
        if cleaned != str(tn):
            tn.replace_with(cleaned)
    return soup


# -- helpers --------------------------------------------------------


def _strip_empty_tags(soup: BeautifulSoup) -> None:
    """Remove tags with no visible content (preserve void elements)."""
    VOID = frozenset({"br", "hr", "img", "input", "col", "embed", "source", "wbr"})
    changed = True
    while changed:
        changed = False
        for tag in soup.find_all(True):
            if tag.name in VOID:
                continue
            if not tag.get_text(strip=True) and not tag.find(list(VOID)):
                tag.decompose()
                changed = True


def _convert_double_br(soup: BeautifulSoup) -> None:
    """Within block parents, turn consecutive <br><br> into <p> splits.

    Preserves inline markup inside the resulting paragraphs.
    """
    SKIP = frozenset({"pre", "code", "table", "thead", "tbody", "tfoot", "tr", "td", "th"})
    for parent in list(soup.find_all(True)):
        if parent.name in SKIP:
            continue
        if not _has_double_br(parent):
            continue
        _split_on_double_br(parent, soup)


def _has_double_br(parent: Tag) -> bool:
    run = 0
    for child in parent.children:
        if isinstance(child, Tag) and child.name == "br":
            run += 1
            if run >= 2:
                return True
        elif isinstance(child, NavigableString) and not str(child).strip():
            continue  # whitespace between <br>s
        else:
            run = 0
    return False


def _split_on_double_br(parent: Tag, soup: BeautifulSoup) -> None:
    """Replace children of *parent* with <p> wrappers split at double-<br>."""
    children = list(parent.children)

    segments: list[list] = [[]]
    br_run = 0
    pending_single_br = False

    for child in children:
        if isinstance(child, Tag) and child.name == "br":
            br_run += 1
            if br_run >= 2:
                # Start new segment
                if segments[-1]:
                    segments.append([])
                br_run = 0
                pending_single_br = False
            continue
        # whitespace-only NavigableString between <br>s → don't reset run
        if isinstance(child, NavigableString) and not str(child).strip():
            if br_run >= 1:
                continue
        # Real content
        if br_run == 1:
            # single <br> → just a line break within segment
            segments[-1].append(soup.new_tag("br"))
        br_run = 0
        segments[-1].append(child.extract() if hasattr(child, "extract") else child)

    if len(segments) <= 1:
        return

    # Remove all old children
    parent.clear()

    for seg in segments:
        if not seg:
            continue
        p = soup.new_tag("p")
        for item in seg:
            p.append(item)
        parent.append(p)


