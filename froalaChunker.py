#  4. Table Classification
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━


def _find_own(table_tag: Tag, names: str | list[str], **kw: Any) -> list[Tag]:
    """Find elements belonging directly to *this* table, not nested ones."""
    result: list[Tag] = []
    if isinstance(names, str):
        names = [names]
    for el in table_tag.find_all(names, recursive=True, **kw):
        if el.find_parent("table") is table_tag:
            result.append(el)
    return result


def classify_table(table_tag: Tag) -> tuple[str, dict]:
    """Classify a <table> as ``'data'``, ``'layout'``, or ``'unknown'``.

    Only inspects elements that belong directly to this table (not
    nested <table> descendants).

    Returns ``(kind, reason_dict)`` with scoring details.
    """
    reason: dict[str, Any] = {}

    # ── signals (own only) ─────────────────────────────────────────
    has_caption = bool(_find_own(table_tag, "caption"))
    has_thead = bool(_find_own(table_tag, "thead"))
    own_ths = _find_own(table_tag, "th")
    has_th = bool(own_ths)
    has_scope = any(t.get("scope") for t in own_ths)
    has_headers_attr = bool(_find_own(table_tag, ["td", "th"], attrs={"headers": True}))

    role = (table_tag.get("role") or "").lower()
    presentation_role = role in ("presentation", "none")

    nested_tables = len(table_tag.find_all("table", recursive=True))

    all_text = _tag_text(table_tag)
    links = table_tag.find_all("a", recursive=True)
    link_text_len = sum(len(_tag_text(a)) for a in links)
    link_density = link_text_len / max(len(all_text), 1)

    own_rows = _find_own(table_tag, "tr")
    col_counts = [len(r.find_all(["td", "th"], recursive=False)) for r in own_rows]
    uniform_cols = len(set(col_counts)) <= 1 and len(col_counts) > 1

    imgs = table_tag.find_all("img", recursive=True)
    forms = table_tag.find_all(["input", "button", "select", "textarea"], recursive=True)
    interactive = len(imgs) + len(forms) + len(links)
    own_cells = _find_own(table_tag, ["td", "th"])
    total_cells = len(own_cells)

    reason.update(
        {
            "has_caption": has_caption,
            "has_th": has_th,
            "has_thead": has_thead,
            "has_scope": has_scope,
            "has_headers_attr": has_headers_attr,
            "presentation_role": presentation_role,
            "link_density": round(link_density, 3),
            "nested_tables": nested_tables,
            "row_count": len(col_counts),
            "uniform_columns": uniform_cols,
            "interactive_elements": interactive,
            "total_cells": total_cells,
        }
    )

    # ── scoring ────────────────────────────────────────────────────
    ds = 0.0  # data_score
    ls = 0.0  # layout_score

    if has_caption:
        ds += 0.25
    if has_th:
        ds += 0.20
    if has_thead:
        ds += 0.20
    if has_scope:
        ds += 0.15
    if has_headers_attr:
        ds += 0.10
    if uniform_cols and len(col_counts) >= 2:
        ds += 0.10

    if presentation_role:
        ls += 0.35
    if nested_tables > 0:
        ls += 0.15
    if link_density > 0.5:
        ls += 0.15
    if 0 < total_cells <= 3:
        ls += 0.10
    if interactive > 3 and total_cells > 0:
        ls += 0.10

    # edge: presentation + data signals → pull toward unknown
    if presentation_role and (has_th or has_caption):
        ls -= 0.15
        ds += 0.10

    if ds > ls and ds >= 0.30:
        kind = "data"
        conf = min(1.0, ds / max(ds + ls, 0.01))
    elif ls > ds and ls >= 0.20:
        kind = "layout"
        conf = min(1.0, ls / max(ds + ls, 0.01))
    else:
        kind = "unknown"
        conf = 0.30

    reason.update(
        {
            "data_score": round(ds, 3),
            "layout_score": round(ls, 3),
            "estimated_kind": kind,
            "confidence": round(conf, 3),
        }
    )
    return kind, reason


