#  11. Pretty-print helper
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━


def print_chunks(chunks: list[Chunk], *, max_embed: int = 250) -> None:
    """Debug printer."""
    print(f"\n{'=' * 72}")
    print(f"  Total chunks: {len(chunks)}")
    print(f"{'=' * 72}")
    for i, c in enumerate(chunks):
        ep = c.embedding_text[:max_embed]
        if len(c.embedding_text) > max_embed:
            ep += "…"
        print(f"\n─── Chunk {i} ───")
        print(f"  type:         {c.chunk_type}")
        print(f"  heading_path: {c.heading_path}")
        print(f"  tokens (est): {c.metadata.get('token_estimate', '?')}")
        for k in ("table_kind", "table_confidence", "region_role", "row_index", "block_types", "dom_path"):
            if k in c.metadata:
                val = c.metadata[k]
                if k == "dom_path" and isinstance(val, list):
                    val = val[:2]
                print(f"  {k}: {val}")
        print(f"  embedding_text:\n    {ep}")
    print()


