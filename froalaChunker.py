#  5. Block Extraction (recursive DOM walk)
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━


def extract_blocks(
    node: Tag,
    heading_state: dict[str, list],
    config: ParseConfig,
    dom_prefix: str = "",
) -> list[dict]:
    """Recursively walk the DOM and return a flat list of *block dicts*.

    Block dict keys:
        type:          heading | paragraph | list | table | image |
                       preformatted | blockquote
        tag:           original Tag (or None for bare text)
        text:          plain text
        html:          cleaned HTML
        heading_path:  current heading breadcrumb
        dom_path:      CSS-like path
        level:         heading level (headings only)
    """
    blocks: list[dict] = []

    if isinstance(node, NavigableString):
        text = str(node).strip()
        if text:
            blocks.append(_block("paragraph", None, text, html_mod.escape(text), heading_state, dom_prefix))
        return blocks

    if not isinstance(node, Tag):
        return blocks

    name = (node.name or "").lower()
    path = f"{dom_prefix}>{name}" if dom_prefix else name

    if name in REMOVE_TAGS:
        return blocks

    # ── heading ────────────────────────────────────────────────────
    if name in HEADING_TAGS:
        level = _heading_level(name)
        text = _tag_text(node)
        if text:
            _update_heading_state(heading_state, level, text)
            blocks.append(
                _block("heading", node, text, _tag_html(node), heading_state, path, level=level)
            )
        return blocks

    # ── table ──────────────────────────────────────────────────────
    if name == "table":
        blocks.append(_block("table", node, _tag_text(node), _tag_html(node), heading_state, path))
        return blocks

    # ── img ─────────────────────────────────────────────────────────
    if name == "img":
        alt = node.get("alt", "")
        blocks.append(
            _block("image", node, f"[Image: {alt}]" if alt else "[Image]", _tag_html(node), heading_state, path)
        )
        return blocks

    # ── pre ─────────────────────────────────────────────────────────
    if name == "pre":
        blocks.append(_block("preformatted", node, node.get_text(), _tag_html(node), heading_state, path))
        return blocks

    # ── blockquote ──────────────────────────────────────────────────
    if name == "blockquote":
        blocks.append(_block("blockquote", node, _tag_text(node), _tag_html(node), heading_state, path))
        return blocks

    # ── list ────────────────────────────────────────────────────────
    if name in ("ul", "ol"):
        blocks.append(_block("list", node, _tag_text(node), _tag_html(node), heading_state, path))
        return blocks

    # ── paragraph ──────────────────────────────────────────────────
    if name == "p":
        text = _tag_text(node)
        if text:
            blocks.append(_block("paragraph", node, text, _tag_html(node), heading_state, path))
        return blocks

    # ── div: container vs leaf ─────────────────────────────────────
    if name == "div":
        has_block_child = any(isinstance(c, Tag) and c.name in BLOCK_TAGS for c in node.children)
        if has_block_child:
            for child in node.children:
                blocks.extend(extract_blocks(child, heading_state, config, path))
        else:
            text = _tag_text(node)
            if text:
                blocks.append(_block("paragraph", node, text, _tag_html(node), heading_state, path))
        return blocks

    # ── fallback: recurse ──────────────────────────────────────────
    for child in node.children:
        blocks.extend(extract_blocks(child, heading_state, config, path))

    return blocks


def _block(
    btype: str,
    tag: Tag | None,
    text: str,
    html_str: str,
    hs: dict,
    dom_path: str,
    *,
    level: int = 0,
) -> dict:
    d: dict[str, Any] = {
        "type": btype,
        "tag": tag,
        "text": text,
        "html": html_str,
        "heading_path": list(hs["path"]),
        "dom_path": dom_path,
    }
    if level:
        d["level"] = level
    return d


def _update_heading_state(hs: dict[str, list], level: int, text: str) -> None:
    """Trim the heading stack and push the new heading."""
    while hs["levels"] and hs["levels"][-1] >= level:
        hs["path"].pop()
        hs["levels"].pop()
    hs["path"].append(f"[H{level}] {text}")
    hs["levels"].append(level)


